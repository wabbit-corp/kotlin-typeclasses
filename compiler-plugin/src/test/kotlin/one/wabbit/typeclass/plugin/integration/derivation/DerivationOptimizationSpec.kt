/**
 * Specification for a future derivation-optimization pass.
 *
 * Goal:
 * - Keep the current derivation contract and observable semantics.
 * - Replace metadata-interpreter hot paths with code that is as close as possible to what a
 *   careful human would have handwritten for the same ADT.
 * - Treat `@Derive(JsonCodec::class)` as an opportunity to emit direct encode/decode logic for
 *   the concrete derived target, not merely a generic metadata-driven interpreter object.
 * - Treat `@DeriveVia(JsonCodec::class, Foo::class)` as an opportunity to keep only the minimal
 *   direct transport composition on the hot path.
 *
 * Current architecture:
 * - Derivation today is intentionally generic. The plugin emits metadata plus prerequisite
 *   instance slots, and the typeclass companion's `deriveProduct` / `deriveSum` / `deriveEnum`
 *   methods construct the runtime instance from that metadata.
 * - That design is flexible and semantically correct, but it leaves an optimization gap:
 *   many hot-path operations still iterate metadata, read field descriptors indirectly, scan
 *   sum cases linearly, or bounce through transport adapters whose structure is statically
 *   known at the derivation site.
 *
 * Desired end state:
 * - For local product derivation, the generated codec should look morally like:
 *
 *   `object : JsonCodec<Person> {`
 *   `  override fun encode(value: Person): JsonElement = buildJsonObject { ... direct field calls ... }`
 *   `  override fun decode(json: JsonElement): Person = Person(name = ..., age = ...)`
 *   `}`
 *
 * - For local sealed sums, the generated codec should look morally like:
 *
 *   `override fun encode(value: Expr): JsonElement = when (value) {`
 *   `  is Lit -> ...`
 *   `  is Add -> ...`
 *   `  End -> ...`
 *   `}`
 *
 *   and `decode` should dispatch directly on the discriminator rather than scanning candidate
 *   metadata records.
 *
 * - For `@DeriveVia(JsonCodec::class, Foo::class)` or pinned-Iso paths, the generated codec
 *   should look morally like:
 *
 *   `override fun encode(value: UserId): JsonElement = fooCodec.encode(userIdToFoo(value))`
 *   `override fun decode(json: JsonElement): UserId = fooToUserId(fooCodec.decode(json))`
 *
 *   with the transport chain composed once in generated code.
 *
 * Current `DeriveVia` status:
 * - The current backend already resolves the transport path at compile time and emits a bespoke
 *   adapter object; it is not doing runtime path search on every call.
 * - The remaining `DeriveVia` optimization target is therefore narrower than the plain `@Derive`
 *   target:
 *   remove unnecessary adapter allocation where possible, collapse identity transports, fuse
 *   local transport chains, and eliminate dead unwrap/rewrap pairs.
 *
 * Optimization scope:
 * - The pass should specialize plugin-generated derivation scaffolding.
 * - It may partially evaluate plugin-generated metadata access, field/case selection, direct
 *   constructor reconstruction, discriminator constants, enum-entry tables, and transport-plan
 *   composition.
 * - It should not attempt arbitrary global purity inference or arbitrary symbolic execution of
 *   user code.
 *
 * Safety / boundary rules:
 * - No cross-module inlining of user-authored codec logic.
 * - No cross-compilation-unit inlining of user-authored codec logic, even within the same module.
 *   A compilation unit here means a Kotlin source file.
 * - The pass may still reference stable external symbols directly:
 *   companion instances, top-level singleton instances, enum entries, constructors, getters,
 *   and pure plugin-generated transport helpers.
 * - If code lives in another compilation unit or module, generated code should call or load it,
 *   not clone its body.
 * - This avoids invalidating unrelated files too aggressively, keeps dynamic-link behavior sane,
 *   and avoids baking dependency internals into generated bytecode.
 *
 * Allowed local specialization:
 * - Within the same compilation unit, the pass may inline or partially evaluate a conservative
 *   whitelist of shapes when their full bodies are available and obviously stable:
 *   `const` values, enum entries, direct constructor calls, transparent value-class unwrap/rewrap,
 *   direct property getters on local data/value classes, pure plugin-generated lambdas, and tiny
 *   helper functions that are syntactically first-order and side-effect-free.
 * - This is intentionally narrower than "arbitrary pure Kotlin".
 * - If the proof obligation is not trivial, keep a direct call boundary instead of guessing.
 *
 * Observable success criteria:
 * - Product encode/decode hot paths have no iteration over `metadata.fields`.
 * - Sum encode/decode hot paths have no linear scan over `metadata.cases`.
 * - Enum encode/decode hot paths use direct entry-name tables or direct `when` dispatch.
 * - `DeriveVia` encode/decode hot paths invoke each transport boundary at most once per entry/exit.
 * - Recursive derivations allocate indirection cells only where the graph is actually recursive.
 * - Cross-file and cross-module helpers remain as symbol references rather than cloned bodies.
 *
 * Non-goals:
 * - Whole-program optimization.
 * - General partial evaluation of arbitrary user-authored Kotlin.
 * - Rewriting foreign-module helper bodies into the current module.
 * - Depending on backend JITs to recover obvious structure we can emit directly ourselves.
 *
 * Proposed implementation pipeline:
 * 1. Lower ordinary derivation first, producing an explicit derivation plan.
 * 2. Recognize optimizeable `JsonCodec`-shaped plans and `DeriveVia(JsonCodec, ...)` plans.
 * 3. Normalize transport plans and metadata access into a small internal SSA-like shape.
 * 4. Perform local, boundary-aware partial evaluation on that internal shape.
 * 5. Emit direct IR for products, sums, enums, and via-compositions.
 * 6. Run a tiny peephole pass to collapse identities, dead temporaries, redundant casts, and
 *    direct unwrap/rewrap pairs.
 *
 * Evidence / invalidation model:
 * - Local generated code may depend structurally on the current file's ADT shape.
 * - It may depend nominally on external helpers and external prerequisite instances through
 *   stable calls and loads only.
 * - Changes to an external helper implementation should not require the current file to be
 *   recompiled merely because the optimizer had previously cloned that implementation. The pass
 *   should therefore avoid such cloning.
 *
 * Test philosophy in this suite:
 * - These tests are specification fixtures first.
 * - Many are semantic scaffolds plus shape contracts in comments.
 * - Passing semantic output alone is not enough to prove optimization.
 * - For local interpreter-elimination cases, the fixtures therefore also define explicit counters
 *   for metadata-loop and case-scan hot paths; once the optimization pass exists those counters
 *   should drop to zero in the optimized fixtures.
 * - Those counters still do not prove every shape claim: constant field-name literals, direct
 *   discriminator literals, fused allocation shape, and direct symbol references across file
 *   boundaries will still need future IR/bytecode assertions.
 * - For cross-compilation-unit and cross-module boundary cases, semantic output alone still does
 *   not prove that a call boundary was preserved, so future work should extend the harness with
 *   IR/bytecode assertions and, for cross-module cases, dependency-swap validation.
 * - The recursive and `DeriveVia` fixtures are especially important examples of this limitation:
 *   the current counters do not observe recursive cell placement, allocation shape, or wrapper
 *   fusion by themselves.
 * - The comments under each test state the intended generated shape and the boundary rules that
 *   should hold once the optimization pass exists.
 *
 * JSON ordering policy in this suite:
 * - Field and discriminator ordering is intentionally part of the current spec.
 * - The target is "close to a straightforward handwritten codec", and a straightforward handwritten
 *   codec would usually emit fields in declaration order with the discriminator first for sums.
 */
@Ignore("Spec-only: derivation optimization pass is not implemented yet")
class DerivationOptimizationSpec : IntegrationTestSupport() {
    private val serializationRuntime = listOf(CompilerHarnessPlugin.SerializationRuntime)

    private val jsonCodecPrelude =
        """
        package demo

        import kotlinx.serialization.json.Json
        import kotlinx.serialization.json.JsonElement
        import kotlinx.serialization.json.JsonObject
        import kotlinx.serialization.json.JsonPrimitive
        import kotlinx.serialization.json.boolean
        import kotlinx.serialization.json.buildJsonObject
        import kotlinx.serialization.json.int
        import kotlinx.serialization.json.jsonObject
        import kotlinx.serialization.json.jsonPrimitive
        import one.wabbit.typeclass.Derive
        import one.wabbit.typeclass.DeriveVia
        import one.wabbit.typeclass.EnumTypeclassMetadata
        import one.wabbit.typeclass.Instance
        import one.wabbit.typeclass.Iso
        import one.wabbit.typeclass.ProductTypeclassMetadata
        import one.wabbit.typeclass.SumTypeclassMetadata
        import one.wabbit.typeclass.Typeclass
        import one.wabbit.typeclass.TypeclassDeriver
        import one.wabbit.typeclass.get
        import one.wabbit.typeclass.matches
        import one.wabbit.typeclass.summon

        enum class JsonCodecRepresentation {
            INLINE,
            OBJECT,
        }

        object OptimizationCounters {
            var productEncodeMetadataLoops: Int = 0
            var productDecodeMetadataLoops: Int = 0
            var sumEncodeCaseScans: Int = 0
            var sumDecodeCaseScans: Int = 0
            var enumDecodeLinearScans: Int = 0
        }

        fun resetOptimizationCounters() {
            OptimizationCounters.productEncodeMetadataLoops = 0
            OptimizationCounters.productDecodeMetadataLoops = 0
            OptimizationCounters.sumEncodeCaseScans = 0
            OptimizationCounters.sumDecodeCaseScans = 0
            OptimizationCounters.enumDecodeLinearScans = 0
        }

        fun snapshotOptimizationCounters(): String =
            listOf(
                OptimizationCounters.productEncodeMetadataLoops,
                OptimizationCounters.productDecodeMetadataLoops,
                OptimizationCounters.sumEncodeCaseScans,
                OptimizationCounters.sumDecodeCaseScans,
                OptimizationCounters.enumDecodeLinearScans,
            ).joinToString(separator = "|")

        @Typeclass
        interface JsonCodec<A> {
            val representation: JsonCodecRepresentation

            fun encode(value: A): JsonElement

            fun decode(json: JsonElement): A

            companion object : TypeclassDeriver {
                override fun deriveProduct(metadata: ProductTypeclassMetadata): Any =
                    object : JsonCodec<Any?> {
                        override val representation: JsonCodecRepresentation =
                            if (metadata.isValueClass) {
                                JsonCodecRepresentation.INLINE
                            } else {
                                JsonCodecRepresentation.OBJECT
                            }

                        override fun encode(value: Any?): JsonElement {
                            require(value != null)
                            if (metadata.isValueClass) {
                                val field = metadata.fields.single()
                                val fieldCodec = field.instance as JsonCodec<Any?>
                                return fieldCodec.encode(field.get(value))
                            }
                            OptimizationCounters.productEncodeMetadataLoops += 1
                            return buildJsonObject {
                                metadata.fields.forEach { field ->
                                    val fieldCodec = field.instance as JsonCodec<Any?>
                                    put(field.name, fieldCodec.encode(field.get(value)))
                                }
                            }
                        }

                        override fun decode(json: JsonElement): Any? {
                            if (metadata.isValueClass) {
                                val field = metadata.fields.single()
                                val fieldCodec = field.instance as JsonCodec<Any?>
                                return metadata.construct(fieldCodec.decode(json))
                            }
                            val jsonObject = json.jsonObject
                            OptimizationCounters.productDecodeMetadataLoops += 1
                            val fieldValues =
                                metadata.fields.map { field ->
                                    val fieldCodec = field.instance as JsonCodec<Any?>
                                    fieldCodec.decode(jsonObject.getValue(field.name))
                                }
                            return metadata.construct(*fieldValues.toTypedArray())
                        }
                    }

                override fun deriveSum(metadata: SumTypeclassMetadata): Any =
                    object : JsonCodec<Any?> {
                        override val representation: JsonCodecRepresentation = JsonCodecRepresentation.OBJECT

                        override fun encode(value: Any?): JsonElement {
                            require(value != null)
                            OptimizationCounters.sumEncodeCaseScans += 1
                            val selected = metadata.cases.single { candidate -> candidate.matches(value) }
                            val caseCodec = selected.instance as JsonCodec<Any?>
                            val payload = caseCodec.encode(value)
                            return when (caseCodec.representation) {
                                JsonCodecRepresentation.INLINE ->
                                    buildJsonObject {
                                        put("type", JsonPrimitive(selected.name))
                                        put("value", payload)
                                    }

                                JsonCodecRepresentation.OBJECT ->
                                    buildJsonObject {
                                        put("type", JsonPrimitive(selected.name))
                                        payload.jsonObject.forEach { (key, fieldValue) -> put(key, fieldValue) }
                                    }
                            }
                        }

                        override fun decode(json: JsonElement): Any? {
                            val jsonObject = json.jsonObject
                            val tag = jsonObject.getValue("type").jsonPrimitive.content
                            OptimizationCounters.sumDecodeCaseScans += 1
                            val selected = metadata.cases.single { candidate -> candidate.name == tag }
                            val caseCodec = selected.instance as JsonCodec<Any?>
                            val payload =
                                when (caseCodec.representation) {
                                    JsonCodecRepresentation.INLINE -> jsonObject.getValue("value")
                                    JsonCodecRepresentation.OBJECT ->
                                        JsonObject(jsonObject.filterKeys { key -> key != "type" })
                                }
                            return caseCodec.decode(payload)
                        }
                    }

                override fun deriveEnum(metadata: EnumTypeclassMetadata): Any =
                    object : JsonCodec<Any?> {
                        override val representation: JsonCodecRepresentation = JsonCodecRepresentation.INLINE

                        override fun encode(value: Any?): JsonElement {
                            require(value != null)
                            return JsonPrimitive(metadata.entries[metadata.ordinalOf(value)].name)
                        }

                        override fun decode(json: JsonElement): Any? {
                            val name = json.jsonPrimitive.content
                            OptimizationCounters.enumDecodeLinearScans += 1
                            val index = metadata.entries.indexOfFirst { entry -> entry.name == name }
                            require(index >= 0) { "Unknown enum entry: ${'$'}name" }
                            return metadata.valueAt(index)
                        }
                    }
            }
        }

        @Instance
        object IntJsonCodec : JsonCodec<Int> {
            override val representation: JsonCodecRepresentation = JsonCodecRepresentation.INLINE

            override fun encode(value: Int): JsonElement = JsonPrimitive(value)

            override fun decode(json: JsonElement): Int = json.jsonPrimitive.int
        }

        @Instance
        object StringJsonCodec : JsonCodec<String> {
            override val representation: JsonCodecRepresentation = JsonCodecRepresentation.INLINE

            override fun encode(value: String): JsonElement = JsonPrimitive(value)

            override fun decode(json: JsonElement): String = json.jsonPrimitive.content
        }

        @Instance
        object BooleanJsonCodec : JsonCodec<Boolean> {
            override val representation: JsonCodecRepresentation = JsonCodecRepresentation.INLINE

            override fun encode(value: Boolean): JsonElement = JsonPrimitive(value)

            override fun decode(json: JsonElement): Boolean = json.jsonPrimitive.boolean
        }

        fun compact(json: JsonElement): String = Json { prettyPrint = false }.encodeToString(JsonElement.serializer(), json)

        context(codec: JsonCodec<A>)
        fun <A> roundTrip(value: A): A = codec.decode(codec.encode(value))

        context(codec: JsonCodec<A>)
        fun <A> encodeToString(value: A): String = compact(codec.encode(value))
        """.trimIndent()

    @Test
    fun optimizesLocalProductJsonCodecsToStraightLineFieldCode() {
        val source =
            """
            $jsonCodecPrelude

            @Derive(JsonCodec::class)
            data class Person(val name: String, val age: Int, val admin: Boolean)

            fun main() {
                resetOptimizationCounters()
                println(encodeToString(Person("Ada", 3, true)))
                println(roundTrip(Person("Ada", 3, true)))
                println(snapshotOptimizationCounters())
            }
            """.trimIndent()

        // Exact intended optimization semantics:
        // - The optimized codec for Person should not iterate `metadata.fields` on each encode/decode call.
        // - Encode should become straight-line code that reads `value.name`, `value.age`, and `value.admin`
        //   directly and calls the three prerequisite codecs directly.
        // - Decode should become a direct `Person(...)` constructor call, not `metadata.construct(...)`,
        //   whenever the primary constructor is directly available and stable.
        // - The hot path should not read field names from metadata at runtime; JSON key strings should be
        //   constant literals in the generated code.
        // - This is the canonical "be as close to handwritten as possible" product case.
        assertCompilesAndRuns(
            source = source,
            expectedStdout =
                """
                {"name":"Ada","age":3,"admin":true}
                Person(name=Ada, age=3, admin=true)
                0|0|0|0|0
                """.trimIndent(),
            requiredPlugins = serializationRuntime,
        )
    }

