    @Test
    fun observesLocalMultiStepDeriveViaBoundaryCallCounts() {
        val source =
            """
            $jsonCodecPrelude

            object Counters {
                var toMid: Int = 0
                var fromMid: Int = 0
                var toFoo: Int = 0
                var fromFoo: Int = 0
            }

            class Mid(val code: String) {
                override fun equals(other: Any?): Boolean = other is Mid && other.code == code
                override fun hashCode(): Int = code.hashCode()
                override fun toString(): String = "Mid(code=${'$'}code)"
            }

            class Foo(val payload: String) {
                override fun equals(other: Any?): Boolean = other is Foo && other.payload == payload
                override fun hashCode(): Int = payload.hashCode()
                override fun toString(): String = "Foo(payload=${'$'}payload)"
            }

            object UserIdMidIso : Iso<UserId, Mid> {
                override fun to(value: UserId): Mid {
                    Counters.toMid += 1
                    return Mid("mid:${'$'}{value.value}")
                }

                override fun from(value: Mid): UserId {
                    Counters.fromMid += 1
                    return UserId(value.code.removePrefix("mid:").toInt())
                }
            }

            object MidFooIso : Iso<Mid, Foo> {
                override fun to(value: Mid): Foo {
                    Counters.toFoo += 1
                    return Foo("foo:${'$'}{value.code}")
                }

                override fun from(value: Foo): Mid {
                    Counters.fromFoo += 1
                    return Mid(value.payload.removePrefix("foo:"))
                }
            }

            @Instance
            object FooJsonCodec : JsonCodec<Foo> {
                override val representation: JsonCodecRepresentation = JsonCodecRepresentation.OBJECT

                override fun encode(value: Foo): JsonElement =
                    buildJsonObject {
                        put("payload", JsonPrimitive(value.payload))
                    }

                override fun decode(json: JsonElement): Foo =
                    Foo(json.jsonObject.getValue("payload").jsonPrimitive.content)
            }

            @JvmInline
            @DeriveVia(JsonCodec::class, UserIdMidIso::class, MidFooIso::class)
            value class UserId(val value: Int)

            fun main() {
                val codec = summon<JsonCodec<UserId>>()
                println(compact(codec.encode(UserId(4))))
                println(codec.decode(buildJsonObject { put("payload", JsonPrimitive("foo:mid:5")) }))
                println("${'$'}{Counters.toMid}|${'$'}{Counters.fromMid}|${'$'}{Counters.toFoo}|${'$'}{Counters.fromFoo}")
            }
            """.trimIndent()

        // Exact intended optimization semantics:
        // - The optimizer should compose the local transport chain once and emit a direct composite
        //   path rather than nesting generic transport adapters.
        // - `Mid` and `Foo` are intentionally plain nominal classes here so the pinned second segment is
        //   not already structurally transportable by the ordinary Equiv solver.
        // - Encode should morally be `FooJsonCodec.encode(MidFooIso.to(UserIdMidIso.to(value)))`.
        // - Decode should morally be `UserIdMidIso.from(MidFooIso.from(FooJsonCodec.decode(json)))`.
        // - The remaining optimization target is transport fusion and dead-step elimination, not runtime path search.
        // - This fixture observes call counts, not allocation shape. Future shape assertions are still needed to
        //   prove that the chain is represented as one fused plan rather than nested forwarding shells.
        assertCompilesAndRuns(
            source = source,
            expectedStdout =
                """
                {"payload":"foo:mid:4"}
                UserId(value=5)
                1|1|1|1
                """.trimIndent(),
            requiredPlugins = serializationRuntime,
        )
    }

