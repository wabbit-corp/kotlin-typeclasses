    @Test
    fun keepsRecursiveIndirectionOnlyAtTrueRecursiveKnots() {
        val source =
            """
            $jsonCodecPrelude

            @Derive(JsonCodec::class)
            sealed interface Tree

            @Derive(JsonCodec::class)
            data class Leaf(val value: Int) : Tree

            @Derive(JsonCodec::class)
            data class Branch(val left: Tree, val right: Tree) : Tree

